        AppendLine(builder, "    }");
    }

    /// <summary>Appends the values of a generated group snapshot.</summary>
    /// <param name="builder">Source builder.</param>
    /// <param name="orderedTags">Tags in the generated group.</param>
    private static void AppendSnapshotValues(
        StringBuilder builder,
        IReadOnlyList<MitsubishiTagClientGenerator.TagModel> orderedTags)
    {
        for (int i = 0; i < orderedTags.Count; i++)
        {
            var tag = orderedTags[i];
            string suffix = i == orderedTags.Count - 1 ? string.Empty : ",";
            string escapedTagName = tag.Name.Replace("\"", "\\\"");
            AppendLine(builder, $"                [\"{escapedTagName}\"] = ");
            AppendLine(builder, $"                    {SanitizeIdentifier(tag.Name)}{suffix}");
        }
    }
