    /// <param name="optional">Whether the generated result is optional.</param>
    private static void AppendGroupReadMember(
        StringBuilder builder,
        string groupName,
        string snapshotType,
        string escapedGroupName,
        bool optional)
    {
        string nullableMarker = optional ? "?" : string.Empty;
        string methodName = optional ? "ReadOptionalAsync" : "ReadAsync";
        string mapperName = optional ? "TryFromSnapshot" : "FromSnapshot";
        string description = optional ? " when available" : string.Empty;
        AppendMemberDocumentation(builder, $"Reads the {groupName} group snapshot{description}.");
        AppendLine(builder, $"    public async Task<Responce<{snapshotType}{nullableMarker}>> {methodName}(");
        AppendLine(builder, "        CancellationToken cancellationToken = default)");
        AppendLine(builder, IndentedOpenBrace);
        AppendLine(builder, $"        var result = await _owner.ReadTagGroupSnapshotAsync(\"{escapedGroupName}\", ");
        AppendLine(builder, "            cancellationToken).ConfigureAwait(false);");
        AppendLine(builder, "        if (!result.IsSucceed || result.Value is null)");
        AppendLine(builder, NestedOpenBrace);
        AppendLine(builder, $"            return new Responce<{snapshotType}{nullableMarker}>(result);");
        AppendLine(builder, NestedCloseBrace);
        AppendLine(builder);
        AppendLine(builder, $"        return new Responce<{snapshotType}{nullableMarker}>(result, ");
        AppendLine(builder, $"            {snapshotType}.{mapperName}(result.Value));");
        AppendLine(builder, IndentedCloseBrace);
    }

