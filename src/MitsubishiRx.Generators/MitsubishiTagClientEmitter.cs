    /// <summary>Appends a generated snapshot constructor invocation.</summary>
    /// <param name="builder">Source builder.</param>
    /// <param name="snapshotType">Generated snapshot type name.</param>
    /// <param name="orderedTags">Tags in the generated group.</param>
    /// <param name="indentation">Generated source indentation.</param>
    private static void AppendSnapshotConstruction(
        StringBuilder builder,
        string snapshotType,
        IReadOnlyList<MitsubishiTagClientGenerator.TagModel> orderedTags,
        string indentation)
    {
        AppendLine(builder, $"{indentation}return new {snapshotType}(");
        for (int i = 0; i < orderedTags.Count; i++)
        {
            var tag = orderedTags[i];
            string suffix = i == orderedTags.Count - 1 ? string.Empty : ",";
            string escapedTagName = tag.Name.Replace("\"", "\\\"");
            AppendLine(builder, $"{indentation}    snapshot.GetRequired(");
            AppendLine(
                builder,
                $"        new LogicalTagKey<{ResolveReadType(tag.DataType)}>(\"{escapedTagName}\")){suffix}");
        }

        AppendLine(builder, $"{indentation});");
    }

