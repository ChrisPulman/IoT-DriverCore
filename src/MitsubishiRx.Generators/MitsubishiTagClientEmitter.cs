    /// <summary>Appends a generated group snapshot type.</summary>
    /// <param name="builder">Source builder.</param>
    /// <param name="group">Schema group.</param>
    /// <param name="tags">All schema tags.</param>
    private static void AppendGroupSnapshotType(
        StringBuilder builder,
        MitsubishiTagClientGenerator.GroupModel group,
        IReadOnlyList<MitsubishiTagClientGenerator.TagModel> tags)
    {
        string snapshotType = $"{SanitizeIdentifier(group.Name)}Snapshot";
        string escapedGroupName = group.Name.Replace("\"", "\\\"");
        var orderedTags = GetOrderedGroupTags(group, tags);
        AppendSnapshotHeader(builder, snapshotType, orderedTags);
        AppendSnapshotFactoryMember(builder, snapshotType, orderedTags, false);
        AppendLine(builder);
        AppendSnapshotFactoryMember(builder, snapshotType, orderedTags, true);
        AppendLine(builder);
        AppendSnapshotWriteMember(builder, escapedGroupName, orderedTags);
        AppendLine(builder);
        AppendReactiveMappingMembers(builder, snapshotType);
        AppendLine(builder, "}");
        AppendLine(builder);
    }

    /// <summary>Gets the tags included in a generated group snapshot.</summary>
    /// <param name="group">Schema group.</param>
    /// <param name="tags">All schema tags.</param>
    /// <returns>The group tags in schema order.</returns>
    private static List<MitsubishiTagClientGenerator.TagModel> GetOrderedGroupTags(
        MitsubishiTagClientGenerator.GroupModel group,
        IReadOnlyList<MitsubishiTagClientGenerator.TagModel> tags)
        => group.TagNames
            .Select(name => tags.FirstOrDefault(tag => string.Equals(tag.Name, name, StringComparison.Ordinal)))
            .Where(static tag => tag is not null)
            .Cast<MitsubishiTagClientGenerator.TagModel>()
            .ToList();

