    /// <summary>Appends a generated tag client type.</summary>
    /// <param name="builder">Source builder.</param>
    /// <param name="tagName">Schema tag name.</param>
    /// <param name="dataType">Schema data type.</param>
    private static void AppendTagType(StringBuilder builder, string tagName, string? dataType)
    {
        string propertyName = SanitizeIdentifier(tagName);
        string nestedType = $"{propertyName}Tag";
        string escapedTagName = tagName.Replace("\"", "\\\"");
        string readType = ResolveReadType(dataType);
        string readMethod = ResolveReadMethod(dataType);
        string writeType = ResolveWriteType(dataType);
        string writeMethod = ResolveWriteMethod(dataType);

        AppendDocumentation(builder, $"Provides operations for the {tagName} tag.");
        AppendLine(builder, $"public sealed class {nestedType}");
        AppendLine(builder, "{");
        AppendLine(builder, OwnerField);
        AppendMemberDocumentation(builder, $"Initializes the {tagName} tag accessor.");
        AppendLine(builder, $"    public {nestedType}(global::MitsubishiRx.MitsubishiRx owner) => _owner = owner;");
        AppendMemberDocumentation(builder, $"Reads the {tagName} tag.");
        AppendLine(
            builder,
            string.Concat(
                $"    public Task<Responce<{readType}>> ReadAsync(",
                "CancellationToken cancellationToken = default) => ",
                $"_owner.{readMethod}(\"{escapedTagName}\", cancellationToken);"));
        AppendMemberDocumentation(builder, $"Writes the {tagName} tag.");
        AppendLine(
            builder,
            string.Concat(
                $"    public Task<Responce> WriteAsync({writeType} value, ",
                "CancellationToken cancellationToken = default) => ",
                $"_owner.{writeMethod}(\"{escapedTagName}\", value, cancellationToken);"));
        AppendMemberDocumentation(builder, $"Observes updates for the {tagName} tag.");
        AppendLine(
            builder,
            string.Concat(
                $"    public IObservable<MitsubishiReactiveValue<{readType}>> Observe(",
                "TimeSpan pollInterval, TimeSpan? minimumUpdateSpacing = null) => ",
                $"_owner.ObserveReactiveTag(new LogicalTagKey<{readType}>(\"{escapedTagName}\"), ",
                "pollInterval, minimumUpdateSpacing);"));
        AppendLine(builder, "}");
        AppendLine(builder);
    }

