    /// <summary>Converts a schema name into a valid C# identifier.</summary>
    /// <param name="value">Schema name.</param>
    /// <returns>A valid C# identifier.</returns>
    internal static string SanitizeIdentifier(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return "Tag";
        }

        var builder = new StringBuilder();
        bool capitalize = true;
        foreach (char ch in value)
        {
            if (char.IsLetterOrDigit(ch))
            {
                _ = builder.Append(capitalize ? char.ToUpperInvariant(ch) : ch);
                capitalize = false;
            }
            else
            {
                capitalize = true;
            }
        }

        if (builder.Length == 0)
        {
            _ = builder.Append("Tag");
        }

        if (!char.IsLetter(builder[0]) && builder[0] != '_')
        {
            _ = builder.Insert(0, '_');
        }

        return builder.ToString();
    }

    /// <summary>Emits helpers for property-level logical-tag bindings.</summary>
    /// <param name="bindings">The property bindings.</param>
    /// <returns>The generated source.</returns>
    internal static string EmitPropertyBindings(
        IReadOnlyList<MitsubishiTagClientGenerator.PropertyBindingModel> bindings)
    {
        var builder = new StringBuilder();
        AppendLine(builder, "// <auto-generated />");
        foreach (var group in bindings.GroupBy(static binding => (binding.NamespaceName, binding.TypeName)))
        {
            if (!string.IsNullOrWhiteSpace(group.Key.NamespaceName))
            {
                AppendLine(builder, $"namespace {group.Key.NamespaceName}");
                AppendLine(builder, "{");
            }

            AppendLine(builder, $"partial class {group.Key.TypeName}");
            AppendLine(builder, "{");
            foreach (var binding in group)
            {
                var escapedTagName = binding.TagName.Replace("\\", "\\\\").Replace("\"", "\\\"");
                AppendPropertyBinding(builder, binding, escapedTagName);
            }

            AppendLine(builder, "}");
            if (!string.IsNullOrWhiteSpace(group.Key.NamespaceName))
            {
                AppendLine(builder, "}");
            }
        }

        return builder.ToString();
    }

    /// <summary>Appends the generated members for a property-level logical tag binding.</summary>
